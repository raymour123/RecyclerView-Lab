public class CustomObject1 {
    String mTitle1;
    String mDescription1;
    String mColor1;

    public CustomObject1() {
        mTitle1 = "Title1";
        mDescription1 = "Description1";
        mColor1 = "Green";
    }

    public String getmTitle1() {
        return mTitle1;
    }

    public String getmDescription1() {
        return mDescription1;
    }

    public String getmColor1() {
        return mColor1;
    }

    public void setmTitle1(String mTitle1) {
        this.mTitle1 = mTitle1;
    }

    public void setmDescription1(String mDescription1) {
        this.mDescription1 = mDescription1;
    }

    public void setmColor1(String mColor1) {
        this.mColor1 = mColor1;
    }




    }



